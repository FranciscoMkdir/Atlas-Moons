import UIKit.UIFont

struct Okomito {
    static public func light(size: CGFloat) -> UIFont {
        return UIFont(name: "Okomito-Light", size: size)!
    }
    static public func regular(size: CGFloat) -> UIFont {
        return UIFont(name: "Okomito-Regular", size: size)!
    }
    static public func medium(size: CGFloat) -> UIFont {
        return UIFont(name: "Okomito-Medium", size: size)!
    }
    static public func bold(size: CGFloat) -> UIFont {
        return UIFont(name: "Okomito-Bold", size: size)!
    }
    static public func black(size: CGFloat) -> UIFont {
        return UIFont(name: "Okomito-Black", size: size)!
    }
}
